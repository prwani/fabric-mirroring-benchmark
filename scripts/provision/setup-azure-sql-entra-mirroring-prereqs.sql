:setvar FABRIC_ENTRA_PRINCIPAL ""

IF NOT EXISTS (SELECT 1 FROM sys.database_principals WHERE name = N'$(FABRIC_ENTRA_PRINCIPAL)')
BEGIN
  DECLARE @create_fabric_user nvarchar(max) =
    N'CREATE USER ' + QUOTENAME(N'$(FABRIC_ENTRA_PRINCIPAL)') + N' FROM EXTERNAL PROVIDER';
  EXEC sys.sp_executesql @create_fabric_user;
END;

GRANT SELECT TO [$(FABRIC_ENTRA_PRINCIPAL)];
GRANT ALTER ANY EXTERNAL MIRROR TO [$(FABRIC_ENTRA_PRINCIPAL)];
GRANT VIEW DATABASE PERFORMANCE STATE TO [$(FABRIC_ENTRA_PRINCIPAL)];
GRANT VIEW DATABASE SECURITY STATE TO [$(FABRIC_ENTRA_PRINCIPAL)];
GO
