#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=../lib/common.sh
source "$SCRIPT_DIR/../lib/common.sh"

load_env
ROOT="$(repo_root)"
APP_NAME="${FABRIC_SOURCE_APP_NAME:-${PROJECT_NAME:-fpmb}-fabric-mirroring-source}"
VALIDITY_DAYS="${FABRIC_SOURCE_APP_SECRET_VALIDITY_DAYS:-7}"
ENV_OUTPUT="${FABRIC_SOURCE_APP_ENV_OUTPUT:-$ROOT/results/fabric-source-app.env}"

if ! [[ "$VALIDITY_DAYS" =~ ^[1-9][0-9]*$ ]]; then
  echo "FABRIC_SOURCE_APP_SECRET_VALIDITY_DAYS must be a positive integer." >&2
  exit 1
fi

existing_count="$(az ad app list --display-name "$APP_NAME" --query 'length(@)' -o tsv)"
if [[ "$existing_count" != "0" ]]; then
  echo "An app registration named $APP_NAME already exists. Set FABRIC_SOURCE_APP_NAME to a unique value or provide its credentials explicitly." >&2
  exit 1
fi

app_id="$(az ad app create --display-name "$APP_NAME" --sign-in-audience AzureADMyOrg --query appId -o tsv)"
service_principal_id="$(az ad sp create --id "$app_id" --query id -o tsv)"
expires_at="$(date -u -d "+$VALIDITY_DAYS days" +"%Y-%m-%dT%H:%M:%SZ")"
secret="$(az ad app credential reset --id "$app_id" --append --end-date "$expires_at" --query password -o tsv)"
tenant_id="$(az account show --query tenantId -o tsv)"

umask 077
mkdir -p "$(dirname "$ENV_OUTPUT")"
{
  echo "# Generated by create-fabric-source-app.sh. Contains a client secret; do not commit."
  printf 'export FABRIC_SOURCE_TENANT_ID=%q\n' "$tenant_id"
  printf 'export FABRIC_SOURCE_SERVICE_PRINCIPAL_CLIENT_ID=%q\n' "$app_id"
  printf 'export FABRIC_SOURCE_SERVICE_PRINCIPAL_SECRET=%q\n' "$secret"
  printf 'export FABRIC_ENTRA_PRINCIPAL=%q\n' "$APP_NAME"
  printf 'export FABRIC_ENTRA_OBJECT_ID=%q\n' "$service_principal_id"
  printf 'export FABRIC_ENTRA_PRINCIPAL_TYPE=%q\n' "E"
  printf 'export FABRIC_CONNECTION_DISPLAY_NAME=%q\n' "$APP_NAME"
  printf 'export FABRIC_CREATE_CONNECTION=%q\n' "true"
} > "$ENV_OUTPUT"
chmod 600 "$ENV_OUTPUT"

echo "Created source app registration $APP_NAME."
echo "Client ID: $app_id"
echo "Service principal object ID: $service_principal_id"
echo "Secret expires: $expires_at"
echo "Source the protected environment file before granting Azure SQL access and creating the Fabric connection:"
echo "  source $ENV_OUTPUT"
